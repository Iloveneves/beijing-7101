/**/
void FE_498_SaturationComputation(FE_498_Monitor *Monitor_results)
{

	U32 u32_tmp;
	/*
	In order to get a smooth saturation value, this is filtered
	woith a low-pass filter : sat(n) = 0.25*sat(n-1) + 0.75*acc(n)
	FE_498_BER_Reg = accumulator value
	FE_498_Saturation = low-pass filter memory
	*/

	u32_tmp = Monitor_results->FE_498_BER_Reg;
	if(Monitor_results->FE_498_Saturation > u32_tmp)
	{
		Monitor_results->FE_498_Saturation = (9*Monitor_results->FE_498_Saturation)/10;
		Monitor_results->FE_498_Saturation += u32_tmp/10;
	}
	else
	{
		Monitor_results->FE_498_Saturation = (25*Monitor_results->FE_498_Saturation)/100;
		Monitor_results->FE_498_Saturation += (75*u32_tmp)/100;
	}
	if(Monitor_results->FE_498_Saturation > 65535) Monitor_results->FE_498_Saturation = 65535;

	return ;
}
/*****************************************************
**FUNCTION	::	FE_498_GetBERCount
**ACTION	::	return 10^7*BER in u32 and BER in double format
**PARAMS IN ::	*Monitor_results => pointer to the counters structure
**PARAMS OUT::	NONE
**RETURN	::	None
*****************************************************/
void FE_498_GetBERCount(FE_498_Monitor *Monitor_results)
{
	U32 u32_tmp, NByte;
	U8 temp=1;
	U16 timeoutcount = 1000;

	u32_tmp = Get498QAMReg(F498_BERT_ERRCOUNT_L) + (Get498QAMReg(F498_BERT_ERRCOUNT_H)<<8);
	if(Get498QAMReg(F498_BERT_ERR_MODE) == 0) /* rate mode */
	{
		while(timeoutcount>0) /* waiting for the previous count to finish */
		{
			timeoutcount--;
			temp=Get498QAMReg(F498_BERT_ON);
			if (temp == 0)
			{
				break;
			}
		}
		
		Monitor_results->FE_498_BER_Reg = u32_tmp;
		u32_tmp = Get498QAMReg(F498_BERT_NBYTE);
		NByte = PowOf2(2*u32_tmp + 12);
		if(Get498QAMReg(F498_BERT_ERR_SRC) == 0) /* bit count */
		{
			Monitor_results->FE_498_BER_U32 = (50000*Monitor_results->FE_498_BER_Reg);
			Monitor_results->FE_498_BER_U32 /= NByte;
			Monitor_results->FE_498_BER_U32 *= 25;
		}
		else											/* byte count */
		{
			Monitor_results->FE_498_BER_U32 = (50000*Monitor_results->FE_498_BER_Reg);
			Monitor_results->FE_498_BER_U32 /= NByte;
			Monitor_results->FE_498_BER_U32 *= 200;
		}
		Set498QAMReg(F498_BERT_ON,1) ; 			/* Restart a new count */
	}
	else  /* Count mode */
	{
		Monitor_results->FE_498_BER_U32 = u32_tmp;
		
	}
	FE_498_SaturationComputation(Monitor_results);
	return;
}
void FE_498_OptimiseNByteAndGetBER(FE_498_Modulation_t QAMSize,U32 SymbolRate_Bds,FE_498_Monitor *Monitor_results)
{
	U32 WaitingTime, Saturation, NByte,i=0;

	FE_498_GetWaitingTime(QAMSize,SymbolRate_Bds,Monitor_results);
	FE_498_GetBERCount(Monitor_results);

	Saturation = Monitor_results->FE_498_Saturation;
	WaitingTime = Monitor_results->FE_498_WaitingTime;
	NByte = Get498QAMReg(F498_BERT_NBYTE);

	while((((Saturation<10)&(NByte<6))|(((Saturation>32768)|(WaitingTime>3500))&(NByte>0)))&(i<10))
	{
		i++;
		if (((Saturation > 32768)|(WaitingTime>3500))&(NByte>0))
		{
			Set498QAMReg(F498_BERT_NBYTE,NByte-1);
			FE_498_GetWaitingTime(QAMSize,SymbolRate_Bds,Monitor_results);
			WaitingTime = Monitor_results->FE_498_WaitingTime;
			if (WaitingTime<200)
				Set498QAMReg(F498_BERT_NBYTE,NByte);
		}
		if ((Saturation < 10)&&(NByte<6))
		{
			Set498QAMReg(F498_BERT_NBYTE,NByte+1);
			FE_498_GetWaitingTime(QAMSize,SymbolRate_Bds,Monitor_results);
			WaitingTime = Monitor_results->FE_498_WaitingTime;
			if (WaitingTime>3500)
				Set498QAMReg(F498_BERT_NBYTE,NByte);
		}
		FE_498_GetWaitingTime(QAMSize,SymbolRate_Bds,Monitor_results);
		FE_498_GetBERCount(Monitor_results);
		Saturation = Monitor_results->FE_498_Saturation;
		WaitingTime = Monitor_results->FE_498_WaitingTime;
		NByte = Get498QAMReg(F498_BERT_NBYTE);
	}



	return;
}
