/*****************************************************
**FUNCTION	::	FE_498_SetSymbolRate
**ACTION	::	Set symbol frequency
**PARAMS IN	::	AdcClk_Hz	->	ADC frequency (Hz)
**				MasterClk_Hz	->	Master frequency (Hz)
**				SymbolRate	->	symbol rate (bauds)
**				QAMSize		->	QAM size
**PARAMS OUT::	NONE
**RETURN	::	Symbol frequency
*****************************************************/
U32 FE_498_SetSymbolRate(U32 AdcClk_Hz,U32 MasterClk_Hz,U32 SymbolRate, FE_498_Modulation_t QAMSize)
{
	U32 QamSizeCorr=0;
	U32 u32_tmp=0, u32_tmp1=0;
	U32 AdpClk_kHz;

	/* Set Correction factor of SRC gain */
	switch (QAMSize)
	{
		case FE_498_MOD_QAM4:
			QamSizeCorr = 1110;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM16:
			QamSizeCorr = 1032;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM32:
			QamSizeCorr =  954;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM64:
			QamSizeCorr =  983;
			Set498QAMReg(F498_ADJ_AGC_REF,0x82);/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM128:
			QamSizeCorr =  957;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM256:
			QamSizeCorr =  948;
			Set498QAMReg(F498_ADJ_AGC_REF,0x94);/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM512:
			QamSizeCorr =    0;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		case FE_498_MOD_QAM1024:
			QamSizeCorr =  944;
			Set498QAMReg(F498_ADJ_AGC_REF,0x0);	/* Set digital AGC reference */
			break;
		default:
			break;
	}

	/* Transfer ratio calculation */
	if (AdcClk_Hz != 0)
	{
		u32_tmp = 256* SymbolRate;
		u32_tmp = u32_tmp/AdcClk_Hz;
	}
	Set498QAMReg(R498_EQU_CRL_TFR,(U8)u32_tmp);

	/* Symbol rate and SRC gain calculation */
	AdpClk_kHz = (MasterClk_Hz>>1)/1000; /* TRL works at half the system clock */
	if (AdpClk_kHz != 0)
	{
		u32_tmp = SymbolRate;
		u32_tmp1 = SymbolRate;

		if(u32_tmp < 2097152)				/* 2097152 = 2^21 */
		{
			/* Symbol rate calculation */
			u32_tmp *= 2048;				/* 2048 = 2^11 */
			u32_tmp = u32_tmp /AdpClk_kHz;
			u32_tmp = u32_tmp * 16384;		/* 16384 = 2^14 */
			u32_tmp /= 125; 				/* 125 = 1000/2^3 */
			u32_tmp = u32_tmp * 8;			/* 8 = 2^3 */

			/* SRC Gain Calculation */
			u32_tmp1 *= 2048;				/* *2*2^10 */
			u32_tmp1 /= 439;				/* *2/878 */
			u32_tmp1 *= 256;				/* *2^8 */
			u32_tmp1 = u32_tmp1/AdpClk_kHz;	/* /(AdpClk in kHz) */
			u32_tmp1 *= QamSizeCorr*9;		/* *1000*corr factor */
			u32_tmp1 = u32_tmp1/10000000;

		}
		else if(u32_tmp < 4194304)			/* 4194304 = 2**22 */
		{
			/* Symbol rate calculation */
			u32_tmp *= 1024 ;				/* 1024 = 2**10 */
			u32_tmp = u32_tmp /AdpClk_kHz;
			u32_tmp = u32_tmp * 16384;		/* 16384 = 2**14 */
			u32_tmp /= 125; 				/* 125 = 1000/2**3 */
			u32_tmp = u32_tmp * 16; 		/* 16 = 2**4 */

			/* SRC Gain Calculation */
			u32_tmp1 *= 1024;				/* *2*2^9 */
			u32_tmp1 /= 439;				/* *2/878 */
			u32_tmp1 *= 256;				/* *2^8 */
			u32_tmp1 = u32_tmp1/AdpClk_kHz;	/* /(AdpClk in kHz)*/
			u32_tmp1 *= QamSizeCorr*9;		/* *1000*corr factor */
			u32_tmp1 = u32_tmp1/5000000;
		}
		else if(u32_tmp < 8388607)			/* 8388607 = 2**23 */
		{
			/* Symbol rate calculation */
			u32_tmp *= 512 ;				/* 512 = 2**9 */
			u32_tmp = u32_tmp /AdpClk_kHz;
			u32_tmp = u32_tmp * 16384;		/* 16384 = 2**14 */
			u32_tmp /= 125; 				/* 125 = 1000/2**3 */
			u32_tmp = u32_tmp * 32; 		/* 32 = 2**5 */

			/* SRC Gain Calculation */
			u32_tmp1 *= 512;				/* *2*2^8 */
			u32_tmp1 /= 439;				/* *2/878 */
			u32_tmp1 *= 256;				/* *2^8 */
			u32_tmp1 = u32_tmp1/AdpClk_kHz;	/* /(AdpClk in kHz) */
			u32_tmp1 *= QamSizeCorr*9;		/* *1000*corr factor */
			u32_tmp1 = u32_tmp1/2500000;
		}
		else
		{
			/* Symbol rate calculation */
			u32_tmp *= 256 ;				/* 256 = 2**8 */
			u32_tmp = u32_tmp /AdpClk_kHz;
			u32_tmp = u32_tmp * 16384;		/* 16384 = 2**13 */
			u32_tmp /= 125; 				/* 125 = 1000/2**3 */
			u32_tmp = u32_tmp * 64; 		/* 64 = 2**6 */

			/* SRC Gain Calculation */
			u32_tmp1 *= 256;				/* 2*2^7 */
			u32_tmp1 /= 439;				/* *2/878 */
			u32_tmp1 *= 256;				/* *2^8 */
			u32_tmp1 = u32_tmp1/AdpClk_kHz;	/* /(AdpClk in kHz) */
			u32_tmp1 *= QamSizeCorr*9;		/* *1000*corr factor */
			u32_tmp1 = u32_tmp1/1250000;
		}
	}

	FE_498_SetIirAdjacentcoefficient(MasterClk_Hz, SymbolRate);
	FE_498_SetAllPasscoefficient(MasterClk_Hz, SymbolRate);

	/* Replace by one 32 access, see following line.
	Set498QAMReg(F498_SRC_NCO_INC_LL,u32_tmp);
	Set498QAMReg(F498_SRC_NCO_INC_LH,(u32_tmp>>8));
	Set498QAMReg(F498_SRC_NCO_INC_HL,(u32_tmp>>16));
	Set498QAMReg(F498_SRC_NCO_INC_HH,(u32_tmp>>24));*/
	Set498QAMReg(R498_SRC_NCO,u32_tmp);
	
	Set498QAMReg(F498_GAIN_SRC_LO,u32_tmp1&0x00ff) ;
	Set498QAMReg(F498_GAIN_SRC_HI,(u32_tmp1>>8)&0x00ff) ;
	
	return(SymbolRate) ;
}
/*****************************************************
**FUNCTION	::	FE_498_GetSymbolRate
**ACTION	::	Get the current symbol rate
**PARAMS IN	::	MasterClk_Hz->	Master frequency (Hz)
**PARAMS OUT::	NONE
**RETURN	::	Symbol rate
*****************************************************/
U32 FE_498_GetSymbolRate(U32 MasterClk_Hz)
{
	U32 regsym;
	U32 AdpClk_kHz;

	regsym = Get498QAMReg(F498_SRC_NCO_INC_LL)
	         + (Get498QAMReg(F498_SRC_NCO_INC_LH)<<8)
	         + (Get498QAMReg(F498_SRC_NCO_INC_HL)<<16)
	         + (Get498QAMReg(F498_SRC_NCO_INC_HH)<<24);

	AdpClk_kHz = (MasterClk_Hz>>1)/1000; /* TRL works at half the system clock */

	if(regsym < 134217728)				/* 134217728L = 2**27*/
	{
		regsym = regsym * 32;			/* 32 = 2**5 */
		regsym = regsym / 32768;		/* 32768L = 2**15 */
		regsym = AdpClk_kHz * regsym;	/* AdpClk in kHz */
		regsym = regsym/128;			/* 128 = 2**7 */
		regsym *= 125 ;					/* 125 = 1000/2**3 */
		regsym /= 2048;  				/* 2048 = 2**11	*/
	}
	else if(regsym < 268435456) 		/* 268435456L = 2**28 */
	{
		regsym = regsym * 16;			/* 16 = 2**4 */
		regsym = regsym / 32768;		/* 32768L = 2**15 */
		regsym = AdpClk_kHz * regsym;	/* AdpClk in kHz */
		regsym = regsym/128;			/* 128 = 2**7 */
		regsym *= 125 ;					/* 125 = 1000/2**3*/
		regsym /= 1024; 				/* 256 = 2**10*/
	}
	else if(regsym < 536870912) 		/* 536870912L = 2**29*/
	{
		regsym = regsym * 8;			/* 8 = 2**3 */
		regsym = regsym / 32768;		/* 32768L = 2**15 */
		regsym = AdpClk_kHz * regsym;	/* AdpClk in kHz */
		regsym = regsym/128;			/* 128 = 2**7 */
		regsym *= 125 ;					/* 125 = 1000/2**3 */
		regsym /= 512;  				/* 128 = 2**9 */
	}
	else
	{
		regsym = regsym * 4;			/* 4 = 2**2 */
		regsym = regsym / 32768;		/* 32768L = 2**15 */
		regsym = AdpClk_kHz * regsym;	/* AdpClk in kHz */
		regsym = regsym/128;			/* 128 = 2**7 */
		regsym *= 125 ;					/* 125 = 1000/2**3 */
		regsym /= 256;  				/* 64 = 2**8 */
	}
	return(regsym);
}
