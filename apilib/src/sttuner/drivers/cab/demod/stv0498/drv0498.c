/*************NQAM reg functions*************/
int GetNQAMMask(int size, int offset)
{
	S32 i;
	U32 mask=0;
	/*	Create mask	*/
	for (i = 0; i < size; i++)
	{
		mask <<= 1;
		mask +=  1;
	}
	mask = mask << offset;
	return mask;
}

void SetNQAMReg(char*toto1, int addr, int size, int offset, int value)
{
   volatile int mask = GetNQAMMask(size, offset);
   volatile int currentregvalue   = *(int*)addr;
   volatile int currentfieldvalue = ((value << offset) & mask) | (currentregvalue & ~mask);
   *(int*)addr = currentfieldvalue;
}

int GetNQAMReg(char*toto1, int addr, int size, int offset)
{
   volatile int mask = GetNQAMMask(size, offset);
   volatile int currentregvalue   = *(int*)addr;
   volatile int currentfieldvalue = (currentregvalue & mask) >> offset;
   return currentfieldvalue;
}
