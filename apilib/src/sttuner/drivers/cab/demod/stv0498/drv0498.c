/*****************************************************
**FUNCTION	::	FE_498_GetDerotFreq
**ACTION	::	Get the STB0498 derotator frequency
**PARAMS IN	::  AdcClk_Hz	==>	ADC clock frequency (Hz)
**PARAMS OUT::	Derot frequency
**RETURN	::	Derotator frequency (Hz)
*****************************************************/
U32 FE_498_GetDerotFreq(U32 AdcClk_Hz)
{
	U32 Sampled_IF;

	Sampled_IF = Get498QAMReg(F498_MIX_NCO_INC_LL)
				+ (Get498QAMReg(F498_MIX_NCO_INC_HL)<<8)
				+ (Get498QAMReg(F498_MIX_NCO_INC_HH)<<16);

	Sampled_IF /= 256;
	Sampled_IF *= (AdcClk_Hz/1000);
	Sampled_IF += 1;
	Sampled_IF /= 32768;

	return Sampled_IF;
}


