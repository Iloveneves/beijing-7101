ST_ErrorCode_t STPTI_DVBTCLoaderKTNDSL(STPTI_DevicePtr_t CodeStart, void * Params_p)
{
    U32 PTI4_Base = (U32)((U32)CodeStart - 0XC000); 

    STPTI_TCParameters_t * TC_Params_p = Params_p;

#ifdef ST_OSLINUX
    printk(KERN_ALERT "pticore: LoaderFunction called\n");
#endif

    /* Check that the user is using the correct loader for the correct architecture */
    #if defined( TC_PTI4_SECURELITE ) 
        #if !defined( STPTI_ARCHITECTURE_PTI4_SECURE_LITE )
            return( STPTI_ERROR_INVALID_LOADER_OPTIONS );
        #endif

    #elif defined( TC_PTI4_LITE ) 
        #if !defined( STPTI_ARCHITECTURE_PTI4_LITE )
            return( STPTI_ERROR_INVALID_LOADER_OPTIONS );
        #endif

    #else  /* PTI4 */
        #if !defined( STPTI_ARCHITECTURE_PTI4 )
            return( STPTI_ERROR_INVALID_LOADER_OPTIONS );
        #endif
    #endif

    TC_Params_p->TC_CodeStart                  = CodeStart;
    TC_Params_p->TC_CodeSize                   = TRANSPORT_CONTROLLER_CODE_SIZE * sizeof(U32);
    TC_Params_p->TC_DataStart                  = (U32*)(((U32)PTI4_Base) + 0X8000);

    TC_Params_p->TC_LookupTableStart           = (U32*)(((U32)PTI4_Base) + 0X8000);
    TC_Params_p->TC_SystemKeyStart             = (U32*)(((U32)PTI4_Base) + 0X8080);
    TC_Params_p->TC_GlobalDataStart            = (U32*)(((U32)PTI4_Base) + 0X8168);
    TC_Params_p->TC_StatusBlockStart           = (U32*)(((U32)PTI4_Base) + 0X81A0);
    TC_Params_p->TC_MainInfoStart              = (U32*)(((U32)PTI4_Base) + 0X81C4);
    TC_Params_p->TC_DMAConfigStart             = (U32*)(((U32)PTI4_Base) + 0X86C4);
    TC_Params_p->TC_DescramblerKeysStart       = (U32*)(((U32)PTI4_Base) + 0X8CC8);
    TC_Params_p->TC_TransportFilterStart       = (U32*)(((U32)PTI4_Base) + 0X90C4);
    TC_Params_p->TC_SCDFilterTableStart        = (U32*)(((U32)PTI4_Base) + 0X90C4);
    TC_Params_p->TC_PESFilterStart             = (U32*)(((U32)PTI4_Base) + 0X90C4);
    TC_Params_p->TC_SubstituteDataStart        = (U32*)(((U32)PTI4_Base) + 0X8168);
    TC_Params_p->TC_SFStatusStart              = (U32*)(((U32)PTI4_Base) + 0X90C4);
    TC_Params_p->TC_InterruptDMAConfigStart    = (U32*)(((U32)PTI4_Base) + 0X97C4);
    TC_Params_p->TC_SessionDataStart           = (U32*)(((U32)PTI4_Base) + 0X97D4);
    TC_Params_p->TC_EMMStart                   = (U32*)(((U32)PTI4_Base) + 0X9894);
    TC_Params_p->TC_ECMStart                   = (U32*)(((U32)PTI4_Base) + 0X98B0);
    TC_Params_p->TC_VersionID                  = (U32*)(((U32)PTI4_Base) + 0X98D0);

    TC_Params_p->TC_NumberCarousels            = 0X0000;
    TC_Params_p->TC_NumberSystemKeys           = 0X0000;
    TC_Params_p->TC_NumberDMAs                 = 0X0037;
    TC_Params_p->TC_NumberDescramblerKeys      = 0X000F;
    TC_Params_p->TC_SizeOfDescramblerKeys      = 0X0044;
    TC_Params_p->TC_NumberPesFilters           = 0X0000;
    TC_Params_p->TC_NumberSectionFilters       = 0X0040;
    TC_Params_p->TC_NumberSlots                = 0X0040;
    TC_Params_p->TC_NumberOfSessions           = 0X0003;
    TC_Params_p->TC_NumberIndexs               = 0X0040;
    TC_Params_p->TC_NumberTransportFilters     = 0X0000;
    TC_Params_p->TC_NumberSCDFilters           = 0X0000;

    TC_Params_p->TC_SignalEveryTransportPacket = 0X0001;
    TC_Params_p->TC_NumberEMMFilters           = 0X0001;
    TC_Params_p->TC_NumberECMFilters           = 0X0040;
    TC_Params_p->TC_AutomaticSectionFiltering  = FALSE;


    {
        U32 i;
        STPTI_DevicePtr_t DataStart = (U32*)(((U32)CodeStart&0xffff0000) | 0X8000);

        for(i = 0; i < (0X1A00/4); ++i)
        {
            STSYS_WriteRegDev32LE( (U32)&DataStart[i], 0x00 );
        }

        for(i = 0; i < 0X0900; ++i)
        {
            STSYS_WriteRegDev32LE( (U32)&CodeStart[i], 0x00 );
        }

        for(i = 0; i < TRANSPORT_CONTROLLER_CODE_SIZE; ++i)
        {
            STSYS_WriteRegDev32LE( (U32)&CodeStart[i], transport_controller_code[i] );
        }
        
        STSYS_WriteRegDev32LE( (U32)&(TC_Params_p->TC_VersionID[0]), 0X5354 | (0X5054<<16) );
        STSYS_WriteRegDev32LE( (U32)&(TC_Params_p->TC_VersionID[1]), 0X4934 | (0X0003<<16) );
        STSYS_WriteRegDev32LE( (U32)&(TC_Params_p->TC_VersionID[2]), 0X0670 | (0X0000<<16) );
    }

#ifdef ST_OSLINUX
    printk(KERN_ALERT "pticore:TC code loaded\n");
#endif

    return ST_NO_ERROR;

}

